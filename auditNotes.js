    // Components – Reusable UI building blocks (e.g., function Button() { ... }).

    // Hooks – Functions like useState (manage state) and useEffect (side effects).

    // State – Data that changes over time (e.g., const [count, setCount] = useState(0)).

    // Props – Data passed between components (e.g., <Button color="blue" />).

    // JSX – HTML-like syntax inside JavaScript (return <div>Hello</div>).

    // Virtual DOM – React’s efficient way to update the real DOM.

    // Event Handling – Responding to user actions (e.g., onClick={handleClick}).

    // The DOM (Document Object Model) is like a live blueprint of a web page. 
    // When a browser loads HTML, it turns the code into a structured, interactive "tree" of objects that JavaScript can manipulate.